              <h3>Andy</h3><p>

has been providing professional design to satisfied clients for over 15 years. Even while in high school he was producing designs for a tiny clothing company called Drab Designs which he started with his friend Tim Brown. During that time Andy also published an underground, quarterly BMX and music magazine called <a href="http://shapingthepage.com/portfolio/green-bean-zine">Green Bean Zine</a>. Both of these projects fueled his love for design and there was no looking back. While in college he was heavily influenced by the likes of <a href="http://en.wikipedia.org/wiki/David_Carson_(graphic_designer)">David Carson</a>, <a href="http://www.segura-inc.com/about">Carlos Segura</a>, <a href="http://en.wikipedia.org/wiki/Vaughan_Oliver">Vaughan Oliver</a>, and other designers of the day who were pushing conventions in new directions. After graduating with his degree in graphic design, Andy worked as a designer at several companies before the call to independence was too loud to ignore. He went solo as Sans Serif Design. Then a certain cute girl caught his eye and together he and Emily started Glide Design Studio as a part-time venture, and in 2009 Shaping the Page was formed as their full-time design playground. When not pushing pixels Andy enjoys playing with their two children, spinning on his <a href="http://www.elephantglide.com/profile/AndyJohnson">flatland BMX bike</a>, and listening to bands that <a href="http://www.threadless.com/product/917/I_Listen_to_Bands_That_Don_t_Even_Exist_Yet">don&#8217;t even exist yet</a>. Life is good!
